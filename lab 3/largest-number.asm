ASSUME CS:CODE, DS:DATA

DATA SEGMENT
    ARRAY DB 99H, 12H, 56H, 45H, 36H
    COUNT EQU 05H
DATA ENDS

CODE SEGMENT
    START:
    MOV AX,DATA
    MOV DS,AX
    MOV CX,COUNT-1
    MOV SI,OFFSET ARRAY
    XOR AX,AX
    MOV AL,[SI]
    
    LOOP1:
    ADD SI,01H
    CMP [SI],AL
    JC LOOP2
    MOV AL,[SI]

    LOOP2: LOOP LOOP1
    HLT
CODE ENDS
END START